// ─────────────────────────────────────────────────────────────────────────────
// 数值含义（grid 里用这些整数编码颜色层）
//
//  0  透明
//  1  主色 (color 参数，默认 primary 黑)
//  2  主色半透明 50%
//  3  强调色 accent 紫蓝 #93A2FF
//  4  强调色淡 accentLight #E8EDFF
//  5  bonusMint 薄荷绿 #72AEBB
//  6  bonusRose 玫瑰红 #DF6B7A
//  7  bonusBlue 深蓝 #4F67DB
//  8  金黄 #F5C842
//  9  纯白（用于叠加高光）
// ─────────────────────────────────────────────────────────────────────────────

class PixelIconData {
  final List<List<int>> grid;
  const PixelIconData(this.grid);
}

// ─── Widget ──────────────────────────────────────────────────────────────────

class PixelIcon extends StatelessWidget {
  final PixelIconData icon;
  final double size;

  /// 覆盖主色（数值 1 的颜色）。
  /// 留 null 则使用 AppTheme.primary。
  /// 多彩图标建议不传，让 grid 中的色码自然显示。
  final Color? color;

  const PixelIcon({
    super.key,
    required this.icon,
    this.size = 32,
    this.color,
  });

  @override
  Widget build(BuildContext context) {
    return CustomPaint(
      size: Size(size, size),
      painter: _PixelPainter(
        grid: icon.grid,
        primaryColor: color ?? AppTheme.primary,
      ),
    );
  }
}

// ─── Painter ─────────────────────────────────────────────────────────────────

class _PixelPainter extends CustomPainter {
  final List<List<int>> grid;
  final Color primaryColor;

  static const _white = Color(0xFFFFFFFF);

  _PixelPainter({required this.grid, required this.primaryColor});

  Color _resolve(int v) => switch (v) {
        1 => primaryColor,
        2 => primaryColor.withValues(alpha: 0.45),
        3 => AppTheme.accent,          // #93A2FF 紫蓝
        4 => AppTheme.accentLight,     // #E8EDFF 淡紫
        5 => AppTheme.bonusMint,       // #72AEBB 薄荷
        6 => AppTheme.bonusRose,       // #DF6B7A 玫瑰
        7 => AppTheme.bonusBlue,       // #4F67DB 深蓝
        8 => AppTheme.goldAccent,
        9 => _white,                   // 高光白
        _ => primaryColor,
      };

  @override
  void paint(Canvas canvas, Size size) {
    if (grid.isEmpty) return;
    final rows = grid.length;
    final cols = grid[0].length;
    final cellW = size.width / cols;
    final cellH = size.height / rows;
    final cell = min(cellW, cellH);
    final ox = (size.width - cell * cols) / 2;
    final oy = (size.height - cell * rows) / 2;
    final gap = cell * 0.08;
    final paint = Paint()..style = PaintingStyle.fill;

    for (int y = 0; y < rows; y++) {
      for (int x = 0; x < cols; x++) {
        final v = grid[y][x];
        if (v == 0) continue;
        paint.color = _resolve(v);
        canvas.drawRRect(
          RRect.fromRectAndRadius(
            Rect.fromLTWH(
              ox + x * cell + gap,
              oy + y * cell + gap,
              cell - gap * 2,
              cell - gap * 2,
            ),
            Radius.circular(cell * 0.15), // 轻微圆角，像素感中带精致
          ),
          paint,
        );
      }
    }
  }

  @override
  bool shouldRepaint(covariant _PixelPainter old) =>
      old.primaryColor != primaryColor || old.grid != grid;
}

